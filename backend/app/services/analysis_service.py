def generate_candidates_step(db: Session, payload: dict) -> dict:
    episode_id = payload["episode_id"]
    db.execute(delete(Candidate).where(Candidate.episode_id == episode_id))

    candidate_specs = [
        {
            "title_hint": "The meeting line that sounds calm but is not",
            "start_time": 5.0,
            "end_time": 28.0,
            "total_score": 8.9,
            "risk_score": 2.1,
            "risk_level": "low",
        },
        {
            "title_hint": "Why this scene becomes commentary gold",
            "start_time": 10.0,
            "end_time": 33.0,
            "total_score": 8.4,
            "risk_score": 2.6,
            "risk_level": "low",
        },
        {
            "title_hint": "The one line that explains the whole tension",
            "start_time": 15.0,
            "end_time": 37.0,
            "total_score": 8.1,
            "risk_score": 3.1,
            "risk_level": "medium",
        },
    ]

    for index, spec in enumerate(candidate_specs, start=1):
        db.add(
            Candidate(
                episode_id=episode_id,
                candidate_index=index,
                type="context_commentary",
                status=CandidateStatus.GENERATED.value,
                title_hint=spec["title_hint"],
                start_time=spec["start_time"],
                end_time=spec["end_time"],
                duration_seconds=round(spec["end_time"] - spec["start_time"], 3),
                total_score=spec["total_score"],
                risk_score=spec["risk_score"],
                risk_level=spec["risk_level"],
                scores_json={
                    "total_score": spec["total_score"],
                    "hook_score": round(spec["total_score"] + 0.2, 2),
                    "clarity_score": round(spec["total_score"] - 0.3, 2),
                    "commentary_score": round(spec["total_score"] + 0.1, 2),
                },
                risk_reasons=["mock_pipeline", "commentary_density_high"],
                metadata_json={"generated_by": "mock_analysis"},
            )
        )

    episode = db.get(Episode, episode_id)
    if episode is None:
        raise ValueError("Episode not found")
    episode.status = EpisodeStatus.READY.value
    db.add(episode)
    db.commit()

    candidate_count = db.scalar(select(func.count(Candidate.id)).where(Candidate.episode_id == episode_id))
    return {"episode_id": episode_id, "candidate_count": candidate_count}


def candidate_segments(db: Session, candidate: Candidate) -> list[TranscriptSegment]:
    query = (
        select(TranscriptSegment)
        .where(TranscriptSegment.episode_id == candidate.episode_id)
        .where(TranscriptSegment.start_time <= candidate.end_time)
        .where(TranscriptSegment.end_time >= candidate.start_time)
        .order_by(TranscriptSegment.start_time.asc())
    )
    return list(db.scalars(query))


def candidate_shots(db: Session, candidate: Candidate) -> list[Shot]:
    query = (
        select(Shot)
        .where(Shot.episode_id == candidate.episode_id)
        .where(Shot.start_time <= candidate.end_time)
        .where(Shot.end_time >= candidate.start_time)
        .order_by(Shot.shot_index.asc())
    )
    return list(db.scalars(query))
