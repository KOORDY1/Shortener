def ingest_episode_step(db: Session, episode_id: str) -> dict:
    episode = db.get(Episode, episode_id)
    if episode is None:
        raise ValueError("Episode not found")
    if not Path(episode.source_video_path).exists():
        raise FileNotFoundError("Source video file is missing")

    episode.status = EpisodeStatus.PROCESSING.value
    episode.duration_seconds = episode.duration_seconds or 42.0
    episode.fps = episode.fps or 23.976
    episode.width = episode.width or 1920
    episode.height = episode.height or 1080
    episode.metadata_json = {
        **(episode.metadata_json or {}),
        "ingest_mode": "mock",
        "source_verified": True,
    }
    db.add(episode)
    db.commit()

    return {"episode_id": episode.id}


def transcode_proxy_step(db: Session, payload: dict) -> dict:
    episode = db.get(Episode, payload["episode_id"])
    if episode is None:
        raise ValueError("Episode not found")

    episode.proxy_video_path = write_placeholder(
        episode.id,
        ["proxy", "proxy.mp4"],
        "mock proxy artifact generated by transcode_proxy",
    )
    episode.audio_path = write_placeholder(
        episode.id,
        ["audio", "audio.wav"],
        "mock audio artifact generated by transcode_proxy",
    )
    db.add(episode)
    db.commit()
    return payload


def detect_shots_step(db: Session, payload: dict) -> dict:
    episode_id = payload["episode_id"]
    db.execute(delete(Shot).where(Shot.episode_id == episode_id))
    for index in range(8):
        start_time = round(index * 5.0, 3)
        end_time = round(start_time + 4.2, 3)
        thumbnail_path = write_placeholder(
            episode_id,
            ["shots", f"{index + 1:02d}.jpg"],
            f"mock thumbnail for shot {index + 1}",
        )
        db.add(
            Shot(
                episode_id=episode_id,
                shot_index=index + 1,
                start_time=start_time,
                end_time=end_time,
                thumbnail_path=thumbnail_path,
            )
        )
    db.commit()
    return payload


def extract_or_generate_transcript_step(db: Session, payload: dict) -> dict:
    episode_id = payload["episode_id"]
    db.execute(delete(TranscriptSegment).where(TranscriptSegment.episode_id == episode_id))
    for index, line in enumerate(MOCK_TRANSCRIPT):
        start_time = round(index * 5.0 + 0.5, 3)
        end_time = round(start_time + 3.2, 3)
        db.add(
            TranscriptSegment(
                episode_id=episode_id,
                segment_index=index + 1,
                start_time=start_time,
                end_time=end_time,
                text=line,
                speaker_label=f"spk_{(index % 2) + 1}",
            )
        )
    db.commit()
    return payload


